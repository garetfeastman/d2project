export function AvatarIcon({ iconKey, size = 40, className = '' }) {
  // Custom uploaded image
  if (iconKey?.startsWith('/uploads/')) {
    return (
      <div className={`rounded-full overflow-hidden flex-shrink-0 ${className}`} style={{ width: size, height: size }}>
        <img src={iconKey} alt="Avatar" style={{ width: size, height: size, objectFit: 'cover' }} />
      </div>
    )
  }

  // Check mask sprites first
  const mask = MASK_ICONS.find(m => m.key === iconKey)
  if (mask) {
    const scaleX = size / mask.w
    const scaleY = size / mask.h
    return (
      <div
        className={`rounded-full overflow-hidden flex-shrink-0 ${className}`}
        style={{
          width: size,
          height: size,
          backgroundImage: "url('/icons/masks.png')",
          backgroundSize: `${Math.round(855 * scaleX)}px ${Math.round(637 * scaleY)}px`,
          backgroundPosition: `-${Math.round(mask.x * scaleX)}px -${Math.round(mask.y * scaleY)}px`,
          backgroundRepeat: 'no-repeat',
        }}
      />
    )
  }

  // SVG icons
  const icon = AVATAR_ICONS.find(i => i.key === iconKey)
  if (!icon) {
    return (
      <div className={`rounded-full overflow-hidden flex-shrink-0 ${className}`} style={{ width: size, height: size }}>
        <svg viewBox="0 0 100 100" width={size} height={size}>
          <rect width="100" height="100" fill="#0f1827"/>
          <circle cx="50" cy="50" r="48" fill="none" stroke="#f97316" strokeWidth="3"/>
          <text x="50" y="62" textAnchor="middle" fill="#f97316" fontSize="40" fontWeight="bold">?</text>
        </svg>
      </div>
    )
  }
  return (
    <div className={`rounded-full overflow-hidden flex-shrink-0 ${className}`} style={{ width: size, height: size }}>
      <svg viewBox="0 0 100 100" width={size} height={size}>
        {icon.paths}
      </svg>
    </div>
  )
}
